def map13(industry18):
    return IND18_TO_13.get(industry18, "総合商社")  # 不明は汎用枠(総合商社)に戻す


def role_def(industry18, role):
    """会社の18業界 → 13分類 → role定義。三井等の社別固定は呼び出し側で。"""
    cat = map13(industry18)
    return INDUSTRY_ROLES.get(cat, INDUSTRY_ROLES["総合商社"])[role]


